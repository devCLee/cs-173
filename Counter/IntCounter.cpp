#include "IntCounter.h"


//=============================================
IntCounter::IntCounter()
{
	count = 0;
}
//=============================================
IntCounter::IntCounter( int v )
{
	count = v;
}
//=============================================
void IntCounter::increment()
{
	count++;
}
//=============================================
void IntCounter::reset()
{
	count = 0;
}
//=============================================
int	IntCounter::getCount()
{
	return count;
}
