#pragma once

//================================================
template <typename T>
Counter<T>::Counter()
{
	count = 0;
	start = 0;
}

//================================================
template <typename T>
Counter<T>::Counter(T v )
{
	count = v;
	start = v;
}

//================================================
template <typename T>
void Counter<T>::increment()
{
	count++;
}

//================================================
template <typename T>
T Counter<T>::getCount()
{
	return count;
}

//================================================
template <typename T>
void Counter<T>::reset()
{
	count = start;
}

